#' addition_1
#' 
#' dumb function
#'
#' @param x value
#'
#' @return value
#' @export
addition_1 <- function(x = NULL) {
    if(is.null(x)){
        stop("'x' cannot be NULL")
    }
    else{
        return(sum(x))
    }
}

